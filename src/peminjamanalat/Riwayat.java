private void updateDB(String id) {
        try {
            Connection c = dbkoneksi.getConnection();
            Statement s = c.createStatement();
            String q = "UPDATE peminjamanalat "
                    + "SET "
                    + "Status_Pengembalian='Sudah Dikembalikan' "
                    + "WHERE ID_Peminjam="+id;
            s.executeUpdate(q);
        } catch (Exception e) {
        }
    }

    private void addDataToTable(String ID_Peminjam, String Nama_Peminjam, String Alat_Dipinjam, String Tanggal_Peminjaman, String Tanggal_Pengembalian, String Status_Pengembalian) {
        try {
            DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
            int row = jTable1.getRowCount()+1;
            Object[] dataPengembalian = {row,ID_Peminjam,Nama_Peminjam,Nama_Peminjam,Alat_Dipinjam,Tanggal_Peminjaman,Tanggal_Pengembalian,Status_Pengembalian,"Sudah Dikembalikan"};
            m.addRow(dataPengembalian); 
        } catch (Exception e) {
        }
    
    }
