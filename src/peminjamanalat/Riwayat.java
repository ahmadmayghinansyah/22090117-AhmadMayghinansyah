    private void jTFcaririwayatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTFcaririwayatActionPerformed
        // TODO add your handling code here:
      String id = jTFcaririwayat.getText();
    try {
        String q = "SELECT * FROM peminjamanalat WHERE ID_Peminjam=" + id;
        Connection k = dbkoneksi.getConnection();
        Statement s = k.createStatement();
        ResultSet R = s.executeQuery(q);

        while (R.next()) {
            String ID_Peminjam = R.getString("ID_Peminjam"); // Misalkan id diambil dari kolom 'id'
            String Nama_Peminjam = R.getString("Nama_Peminjam");
            String Alat_Dipinjam = R.getString("Alat_Dipinjam");
            String Tanggal_Peminjaman = R.getString("Tanggal_Peminjaman");
            String Tanggal_Pengembalian = R.getString("Tanggal_Pengembalian");
            String Status_Pengembalian = R.getString("Status_Pengembalian");
            // Ambil kolom-kolom lain yang diperlukan dari tabel

            // Pastikan method 'updateDB' dan 'addDataToTable' didefinisikan dengan benar
            updateDB(ID_Peminjam); // Mungkin Anda ingin menyertakan ID_Peminjaman
            addDataToTable(ID_Peminjam, Nama_Peminjam, Alat_Dipinjam, Tanggal_Peminjaman, Tanggal_Pengembalian,
                    Status_Pengembalian); // Sesuaikan dengan kolom-kolom yang diperlukan
        }

        jTFcaririwayat.setText("");
        jTFcaririwayat.requestFocus();

        // Pastikan untuk menutup koneksi dan statement setelah digunakan
        R.close();
        s.close();
        k.close();
    } catch (SQLException e) {
        // Tindakan jika terjadi kesalahan
        e.printStackTrace(); // Ini akan mencetak informasi kesalahan, yang membantu dalam penanganan masalah
    }
    }//GEN-LAST:event_jTFcaririwayatActionPerformed

